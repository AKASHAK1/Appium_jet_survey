    def survey_module(self):
        time.sleep(1)
        self.click(self.click_read_check_box)
        cl.allure_logs("Clicked on Read Check box")
        time.sleep(1)
        self.click(self.click_workout_check_box)
        cl.allure_logs("Clicked on workout Check box")
        time.sleep(1)
        self.click(self.click_video_games_check_box)
        cl.allure_logs("Clicked on video games check box")
        time.sleep(1)
        self.click(self.click_free_time_next_button)
        cl.allure_logs("Clicked on next button")
        time.sleep(1)
        self.click(self.click_spider_man_check_box)
        cl.allure_logs("Spider man selected")
        time.sleep(1)
        self.click(self.click_superhero_next_button)
        cl.allure_logs("clicked on next button")
        time.sleep(1)
        self.click(self.click_outbreak_movie_button)
        cl.allure_logs("Spider man selected")
        time.sleep(1)
        self.click(self.click_movie_next_button)
        cl.allure_logs("clicked on next button")
        time.sleep(1)
