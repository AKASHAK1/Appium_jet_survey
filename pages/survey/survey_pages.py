        cl.allure_logs("clicked on pick a date button")
        time.sleep(1)
        self.click(self.select_date)
        cl.allure_logs("Date Selected")
        time.sleep(1)
        self.click(self.confirm_date_button)
        cl.allure_logs("clicked on ok button after selecting the date")
        time.sleep(1)
