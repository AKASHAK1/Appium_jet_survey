    click_free_time_next_button = ('xpath', '//android.widget.Button[@text="NEXT"]')
    click_spider_man_check_box = ('xpath', '//android.view.View[@text="Spider man ('
                                           'Avengers)"]/android.widget.RadioButton')
    click_superhero_next_button = ('xpath', '//android.widget.Button[@text="NEXT"]')
    click_outbreak_movie_button = ('xpath', '//android.view.View[@text="Outbreak"]/android.widget.RadioButton')
    click_movie_next_button = ('xpath', '//android.widget.Button[@text="NEXT"]')
