        cl.allure_logs("clicked on selfie next button")
        time.sleep(1)
        self.click(self.add_photo_button)
        cl.allure_logs("clicked on add photo button")
        time.sleep(2)
        self.click(self.click_photo_button)
        cl.allure_logs("clicked on camera button")
        time.sleep(2)
        self.click(self.done_photo_button)
        cl.allure_logs("clicked on done photo button")
        time.sleep(2)
        self.click(self.done_selfie_button)
        cl.allure_logs("clicked on done selfie button")
        time.sleep(2)
        self.click(self.done_survey_button)
        cl.allure_logs("clicked on done survey button")
